  function llenarTabla() {
    const tablaProducto = document.getElementById('productos');
    const cuerpoTabla = document.querySelector('#productos tbody');
    tablaProducto.innerHTML="";

    // Clear existing rows
    cuerpoTabla.innerHTML = '';

    const inicio = paginacion * paginacionTotal;
    const fin = inicio + paginacionTotal;
    const filtradoData = data.slice(inicio, fin);

    for (let i = 0; i < filtradoData.length; i++) {
      const p = filtradoData[i];
      let fila = document.createElement('tr');

      let td = document.createElement('td');
      td.innerText = p.id;
      fila.appendChild(td);

      td = document.createElement('td');
      // Mostrar imagen como un elemento de imagen
      const imagen = document.createElement('img');
      imagen.src = p.imagenURL;
      imagen.alt = p.descripcion;
      td.appendChild(imagen);
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.descripcion;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.tipo;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.sexo;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.talla;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.Material;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.Color;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.precio;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.disponiblidad;
      fila.appendChild(td);

      td = document.createElement('td');
      td.innerText = p.sede;
      fila.appendChild(td);

      cuerpoTabla.appendChild(fila);
  }

  tablaProducto.appendChild(cuerpoTabla);
}


