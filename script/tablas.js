function cambiarPag(ubicacion) {
  paginacion = paginacion+ ubicacion;

  if (paginacion < 0) {
    paginacion = 0;
  }

  const ultimaPagina = Math.ceil(data.length / paginacionTotal) - 1;

  if (paginacion > ultimaPagina) {
    paginacion = ultimaPagina;
  }

  llenarTabla();
}

function atras() {
  cambiarPag(-1);
   
}

function sig() {
  cambiarPag(1);  
}

function filtrarPorPrecio() {
  const precioFiltroInput = document.getElementById('filtrarPorPrecio');
  const precioFiltro = parseFloat(precioFiltroInput.value);

  // Verificar si el valor es un número válido
  if (isNaN(precioFiltro)) {
      alert("Por favor, ingrese un valor numérico válido en el campo de filtro.");
      precioFiltroInput.value = ""; // Limpiar el campo de filtro
      return;
  }

  // Filtrar la data en función del precio
  filtradoData = data.filter(item => parseFloat(item.precio) === precioFiltro);

  // Volver a la primera página después de aplicar el filtro
  paginacion = 0;

  // Llenar la tabla con los datos filtrados
  llenarTabla();
  
}